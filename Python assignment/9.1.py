# Define a string
str = "Hello, World!"

# Slicing examples
str1 = str[0:5]  # Characters from index 0 to 4
str2 = str[7:]    # Characters from index 7 to the end
str3 = str[:5]    # Characters from the start to index 4
str4 = str[-6:]    # Last 6 characters

# Print the results
print("Original string:", str)
print("Substring from index 0 to 4:", str1)
print("Substring from index 7 to end:", str2)
print("Substring from start to index 4:", str3)
print("Last 6 characters:", str4)